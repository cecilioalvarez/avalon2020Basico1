package es.avalon;

public class Principal6Arrays3 {
	public static void main(String[] args) {

		// Sumar dos Arrays

		int[] a = new int[] { 2, 4, 9 };
		int[] b = new int[] { 3, 6, 4 };

		System.out.println("La suma de " + a[0] + "+" + b[0] + "=" + (a[0] + b[0]));
		System.out.println("La suma de " + a[1] + "+" + b[1] + "=" + (a[1] + b[1]));
		System.out.println("La suma de " + a[2] + "+" + b[2] + "=" + (a[2] + b[2]));

	}
}
