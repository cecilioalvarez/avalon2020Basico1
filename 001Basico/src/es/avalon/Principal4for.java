package es.avalon;

public class Principal4for {
	
	public static void main(String[] args) {
		
		//Bucle for
		for(int i=0;i<10;i++) {//i++=>i=i+1
			
			System.out.println(i*2);
		}
		//Los 10 primeros numeros pares del 2 al 20
		for(int i=1;i<=10;i++) {
				
				System.out.println(i*2);
		}
	}

}