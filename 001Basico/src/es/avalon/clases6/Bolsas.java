package es.avalon.clases6;

public class Bolsas {
	private int numero;
	private int peso;
	
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}
	public int getPeso() {
		return peso;
	}
	public void setPeso(int peso) {
		this.peso = peso;
	}
	
	public Bolsas(int numero, int peso) {
		super();
		this.setNumero(numero);
		this.setPeso(peso);
	}
	
	
	
	

}
