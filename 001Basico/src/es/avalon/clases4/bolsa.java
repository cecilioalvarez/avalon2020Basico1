package es.avalon.clases4;

public class bolsa {

	private int numero;
	private double peso;
	
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}
	public double getPeso() {
		return peso;
	}
	public void setPeso(double peso) {
		this.peso = peso;
	}
	
	public bolsa(int numero, double peso) {
		super();
		this.numero = numero;
		this.peso = peso;
	}
	
	
	
}
