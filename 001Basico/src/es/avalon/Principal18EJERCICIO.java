package es.avalon;

public class Principal18EJERCICIO {

	public static void main(String[] args) {
		// Funcion con un parametro que imprima un bucle de ese parametro.

		lista(100);

	}

	public static void lista(int a) {
		for (int i = 0; i <= a; i++) {
			System.out.println(i);
		}
		
		
	
	}
}
