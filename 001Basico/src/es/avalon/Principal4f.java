package es.avalon;

public class Principal4f {

	public static void main(String[] args) {


		// Bucle for dentro de un bucle for
		for(int i=0;i<10;i++) {
			for (int j=0;j<=i;j++) {
				System.out.print(j);
				
			}
			System.out.println();
			
		}
		
		/*
		 * 0
		 * 01
		 * 012
		 * 0123
		 * 01234
		 * 012345
		 * 0123456
		 * 01234567
		 * 012345678
		 * 0123456789
		 */
		
	}

}
