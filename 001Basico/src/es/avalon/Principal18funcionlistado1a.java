package es.avalon;

public class Principal18funcionlistado1a {

	public static void main(String[] args) {
		
		//Definir una lista desde 1 hasta a y generar funcion
		
		listado(8);

	}

	private static void listado(int x) {
		for(int i=0;i<=x;i++) {
			System.out.println(i);
		}
	}

}
